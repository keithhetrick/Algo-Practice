// Print all the integers from 1 to 255.

// For Loop
function print1to255() {
  for (let i = 1; i <= 255; i++) {
    console.log(i);
  }
}
print1to255();

// While Loops
function print1to255() {
  let i = 1;
  while (i <= 255) {
    console.log(i);
    i++;
  }
}
print1to255();

function print1to255() {
  let i = 0;
  while (i <= 255) {
    console.log(i);
    i += 1;
  }
}
print1to255();
