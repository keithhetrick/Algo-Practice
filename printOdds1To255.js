// Print all odd integers from 1 to 255

// For Loop
function printOdds1To255() {
  for (let i = 0; i <= 255; i++) {
    i = i + 1;
    console.log(i);
  }
}
printOdds1To255();

// While Loop
function printOdds1To255() {
  let i = 1;
  while (i <= 255) {
    console.log(i);
    i = i + 1;
    i++;
  }
}
printOdds1To255();
